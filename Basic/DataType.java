public class DataType{
    public static void main(String args []){
        // data type in java
        int num = 44;
        float num2 = 4.4f;
        double pi = 3.14;
        char alphabate = 'v';

        System.out.println("Num : "+num);
        System.out.println("Num2 : "+num2);
        System.out.println("Double : "+pi);
        System.out.println("char : "+alphabate);
    }
}